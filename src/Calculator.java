public class Calculator {

    public Integer cal;

    public Integer add(Integer a, Integer b) {
        return a+b;
    }

    public Integer minus(Integer a, Integer b) {
        return a-b;
    }

    public Integer multiply(Integer a, Integer b) {
        return a*b;
    }


    public Double divide(Integer a, Integer b) {
        return (double)a/b;
    }
}
